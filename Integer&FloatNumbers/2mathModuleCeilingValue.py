# find a ceiling value for x - the smallest integer not less than x - we call the appropriate function from the math module: math.ceil(x).
# The syntax for calling functions from modules is always the same: module_name.function_name(argument_1, argument_2, ...)

#find the ceiling value of a given number
import math

x = math.ceil(4.2)

print(x)

y = math.ceil(2.11)

print(y)

